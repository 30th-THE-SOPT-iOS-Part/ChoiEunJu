import UIKit

extension UIColor {
    
    @nonobjc class var darkGray: UIColor {
        return UIColor(red: 114.0 / 255.0, green: 114.0 / 255.0, blue: 114.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var lightGray: UIColor {
        return UIColor(red: 250.0 / 255.0, green: 250.0 / 255.0, blue: 250.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var btnBlue: UIColor {
        return UIColor(red: 55.0 / 255.0, green: 151.0 / 255.0, blue: 239.0 / 255.0, alpha: 1.0)
    }
    
    @nonobjc class var strokeBlack: UIColor {
        return UIColor(red: 0.0, green: 0.0, blue: 0.0, alpha: 0.1)
    }
}
